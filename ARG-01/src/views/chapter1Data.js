// ---------- 模块入口 ----------
export function renderModule(name, area, ctx) {
  area.innerHTML = '';
  const head = document.createElement('div');
  head.className = 'ch1-page-head';
  head.innerHTML = '<span class="ch1-page-title">' + name + '</span><span style="font-size:12px;color:var(--oa-text-light);">2020 年归档材料</span>';
  area.appendChild(head);

  switch (name) {
    case '公文流转':
      renderList(area, NOTICES, ['编号', '日期', '标题', '紧急', '发文部门'], ctx);
      break;
    case '项目管理':
      renderList(area, WEEKLIES, ['编号', '日期', '标题'], ctx);
      break;
    case '工单中心':
      renderList(area, TICKETS, ['工单号', '标题', '状态'], ctx);
      break;
    case '会议纪要':
      renderList(area, MEETINGS, ['编号', '日期', '议题', '参会'], ctx);
      break;
    case '资料中心':
      renderList(area, MISC, ['文件名', '类型'], ctx);
      break;
    case '合同管理':
      renderList(area, CONTRACTS, ['文件名', '类型', '日期'], ctx);
      break;
    case '通讯录':
      renderContacts(area, ctx);
      break;
    case '智能助手':
      renderMuteAbout(area, ctx);
      break;
    default:
      area.innerHTML += '<div class="mod-empty">该模块暂无可查阅的 2020 年归档材料。</div>';
  }
}
