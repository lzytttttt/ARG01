// ---------- 渲染：通用列表 + 详情展开 ----------
function renderList(area, data, cols, ctx) {
  const table = document.createElement('table');
  table.className = 'mod-list';
  const thead = '<thead><tr>' + cols.map(function (c) { return '<th>' + c + '</th>'; }).join('') + '</tr></thead>';
  const rows = data.map(function (d, i) {
    const cells = cols.map(function (c) {
      const key = {
        '编号': 'no', '日期': 'date', '标题': 'title', '工单号': 'no',
        '议题': 'topic', '参会': 'attendees', '文件名': 'name', '类型': 'type',
        '紧急': 'urgent', '状态': 'status', '发文部门': 'dept'
      }[c] || c;
      return '<td>' + (d[key] !== undefined ? d[key] : '') + '</td>';
    }).join('');
    return '<tr data-idx="' + i + '">' + cells + '</tr>';
  }).join('');
  table.innerHTML = thead + '<tbody>' + rows + '</tbody>';
  area.appendChild(table);

  const detailWrap = document.createElement('div');
  detailWrap.className = 'mod-detail-wrap';
  area.appendChild(detailWrap);

  table.querySelectorAll('tbody tr').forEach(function (tr) {
    tr.addEventListener('click', function () {
      const idx = parseInt(tr.getAttribute('data-idx'), 10);
      const d = data[idx];
      table.querySelectorAll('tbody tr').forEach(function (t) { t.classList.remove('is-open'); });
      tr.classList.add('is-open');
      detailWrap.innerHTML = '';
      const detail = document.createElement('div');
      detail.className = 'mod-detail';
      let html = '<div class="src">' + (d.no || d.name || '') + ' · ' + (d.date || '') + '</div>';
      if (d.dept) html += '<div class="src" style="font-size:13px;color:#888;">发文部门：' + d.dept + '</div>';
      if (d.attendees) html += '<div class="src" style="font-size:13px;color:#888;">参会人员：' + d.attendees + '</div>';
      if (d.body) {
        if (d.body.indexOf('<') >= 0) {
          html += '<div style="white-space:pre-line;line-height:2.1;">' + d.body + '</div>';
        } else {
          html += '<p style="white-space:pre-line;line-height:2.1;">' + d.body + '</p>';
        }
      }
      detail.innerHTML = html;
      detailWrap.appendChild(detail);
      if (d.clue) {
        const cl = document.createElement('div');
        cl.className = 'fx-monologue';
        cl.textContent = d.clue;
        detailWrap.appendChild(cl);
        requestAnimationFrame(function () { cl.classList.add('show'); });
      }
    });
  });

  const tip = document.createElement('div');
  tip.style.cssText = 'font-size:12px;color:var(--oa-text-light);margin-top:10px;';
  tip.textContent = '共 ' + data.length + ' 份记录。点击任一行查看详情。';
  area.appendChild(tip);
}

