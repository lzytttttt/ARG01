// ---------- 工具 ----------
function pad(n) { return String(n).padStart(2, '0'); }
function weekDates(startY, startM, startD, n) {
  const d = new Date(startY, startM - 1, startD);
  const out = [];
  for (let i = 0; i < n; i++) {
    out.push(d.getFullYear() + '.' + pad(d.getMonth() + 1) + '.' + pad(d.getDate()));
    d.setDate(d.getDate() + 7);
  }
  return out;
}

