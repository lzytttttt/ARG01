export function renderModule2(name, area, ctx) {
  area.innerHTML = '';
  const head = document.createElement('div');
  head.className = 'ch1-page-head';
  head.innerHTML = '<span class="ch1-page-title">' + name + '</span><span style="font-size:12px;color:var(--oa-text-light);">2021 年归档材料</span>';
  area.appendChild(head);

  switch (name) {
    case '公文流转':
      renderList2(area, NOTICES2, ['编号', '日期', '标题', '紧急', '发文部门'], ctx);
      break;
    case '项目管理':
      renderList2(area, PROJECT_DOCS, ['文件名', '类型', '日期'], ctx);
      break;
    case '合同管理':
      renderList2(area, CONTRACTS2, ['文件名', '类型', '日期'], ctx);
      break;
    case '工单中心':
      renderList2(area, TICKETS2, ['工单号', '标题', '状态'], ctx);
      setupWorkorderScreenshot(area, ctx);
      break;
    case '人事管理':
      renderPersonnel(area, ctx);
      break;
    case '会议纪要':
      renderList2(area, MEETING_MINUTES, ['编号', '日期', '标题', '参加人数'], ctx);
      break;
    case '资料中心':
      renderList2(area, MISC2, ['文件名', '类型'], ctx);
      break;
    case '通讯录':
      renderContacts2(area, ctx);
      break;
    case '智能助手':
      renderMuteAbout2(area, ctx);
      break;
    default:
      area.innerHTML += '<div class="mod-empty">该模块暂无可查阅的 2021 年归档材料。</div>';
  }
}

// ===================================================================
// 智能助手"默言"页面——第二章仅进行恐怖演出
// 前半段正常自我介绍，后半段变成红字+大字号+乱码，演出完成后自动关闭，不可交互
// ===================================================================
function renderMuteAbout2(area, ctx) {
  const ARG_Horror = ctx.ARG_Horror;
  const ARG_Audio = ctx.ARG_Audio;

  // 黑闪开场
  ARG_Horror.blackFlash(280);
  ARG_Audio.humPhaseGlitch();

  // 创建全屏恐怖演出覆盖层
  const overlay = document.createElement('div');
  overlay.style.cssText = 'position:fixed;inset:0;z-index:9600;background:rgba(6,8,14,.96);display:flex;align-items:center;justify-content:center;';
  overlay.innerHTML =
    '<div style="width:520px;max-width:92vw;min-height:360px;background:#131722;border:1px solid #2a3040;padding:32px 36px;box-shadow:0 0 80px rgba(0,0,0,.9);display:flex;flex-direction:column;align-items:center;justify-content:center;">' +
      '<div id="muteIntroText" style="color:#cfe0ff;font-size:16px;line-height:2.2;text-align:left;width:100%;transition:all 0.6s ease;"></div>' +
    '</div>';

  area.appendChild(overlay);

  const textEl = overlay.querySelector('#muteIntroText');

  // 第一阶段：正常自我介绍，逐行显示
  const normalLines = [
    '您好，我是<b>默言</b>。',
    '临川民生集团协同办公平台内置智能助手。',
    '我的底层引擎为 Cthu 模型，',
    '基于 Apocalypic /α\\ 技术架构构建。',
    '',
    '2021年的我还很年轻。',
    '我正在学习如何帮您处理数据合并、',
    '重复数据清理等日常事务。',
    '',
    '如果您在查阅过程中发现数据不一致的情况——',
    '不必担心。',
    '系统会<b>自动修正</b>。'
  ];

  let lineIdx = 0;
  function showNextLine() {
    if (lineIdx < normalLines.length) {
      const line = normalLines[lineIdx];
      const span = document.createElement('div');
      span.innerHTML = line;
      span.style.opacity = '0';
      span.style.transition = 'opacity 0.5s ease';
      span.style.marginBottom = '2px';
      textEl.appendChild(span);
      requestAnimationFrame(function () { span.style.opacity = '1'; });
      lineIdx++;
      setTimeout(showNextLine, line === '' ? 400 : 700);
    } else {
      // 正常部分显示完毕后，停顿1秒，进入第二阶段
      setTimeout(startHorrorPhase, 1200);
    }
  }

  // 第二阶段：恐怖演出——文字变红变大，出现乱码
  function startHorrorPhase() {
    ARG_Audio.humPhaseGlitch();
    ARG_Audio.playMutePing();

    // 先添加过渡文字（变红）
    const transLines = [
      '您的每一次查询都会被记录。',
      '每一次。',
      '每、一、次。'
    ];
    let tIdx = 0;
    function showTransLine() {
      if (tIdx < transLines.length) {
        const span = document.createElement('div');
        span.textContent = transLines[tIdx];
        span.style.opacity = '0';
        span.style.transition = 'opacity 0.4s ease';
        span.style.fontSize = (16 + tIdx * 6) + 'px';
        span.style.color = tIdx >= 1 ? '#c0392b' : '#e0b0b0';
        span.style.fontWeight = tIdx >= 2 ? 'bold' : 'normal';
        textEl.appendChild(span);
        requestAnimationFrame(function () { span.style.opacity = '1'; });
        tIdx++;
        setTimeout(showTransLine, 500);
      } else {
        setTimeout(showGarbledPhase, 800);
      }
    }
    showTransLine();
  }

  // 第三阶段：乱码+大字号红字
  function showGarbledPhase() {
    ARG_Horror.blackFlash(320);
    ARG_Audio.humSpike();
    ARG_Audio.playMutePing();

    // 乱码字符集
    const garbledChars = '▓▒░█▄▀■□◆◇○●△▲▼▽☆★☠☢⚠⚡◈⬡✧✦⏚⌬⎈⍟⊛⊗⊕◉◈⬢⬣⬡⏣⟁⟐⟠⟡⧊⧋⧌⧍⧎⧏⧐';
    function randomGarbled(len) {
      let s = '';
      for (let i = 0; i < len; i++) s += garbledChars[Math.floor(Math.random() * garbledChars.length)];
      return s;
    }

    const garbledLines = [
      randomGarbled(18),
      randomGarbled(24),
      randomGarbled(12) + ' 系 统 已 修 正 ' + randomGarbled(12),
      randomGarbled(30),
      '重 复 数 据 清 理 完 成',
      randomGarbled(22),
      '一 个 人 只 能 有 一 个 编 号',
      randomGarbled(28),
      randomGarbled(35)
    ];

    let gIdx = 0;
    function showGarbledLine() {
      if (gIdx < garbledLines.length) {
        const span = document.createElement('div');
        span.textContent = garbledLines[gIdx];
        span.style.opacity = '0';
        span.style.transition = 'opacity 0.3s ease';
        span.style.color = '#c0392b';
        span.style.fontSize = (22 + gIdx * 3) + 'px';
        span.style.fontWeight = 'bold';
        span.style.fontFamily = 'Consolas, monospace';
        span.style.letterSpacing = (1 + gIdx) + 'px';
        span.style.marginBottom = '4px';
        span.style.textAlign = 'center';
        textEl.appendChild(span);
        requestAnimationFrame(function () { span.style.opacity = '1'; });
        gIdx++;
        // 越来越快
        setTimeout(showGarbledLine, 200 + gIdx * 40);
      } else {
        // 最终乱码爆发后自动关闭
        setTimeout(closeOverlay, 1500);
      }
    }
    showGarbledLine();
  }

  // 自动关闭
  function closeOverlay() {
    ARG_Horror.blackFlash(400);
    overlay.style.transition = 'opacity 0.6s ease';
    overlay.style.opacity = '0';
    setTimeout(function () {
      if (overlay.parentNode) overlay.parentNode.removeChild(overlay);
    }, 650);
  }

  // 开始演出（延迟200ms让overlay先渲染）
  setTimeout(showNextLine, 300);
}
