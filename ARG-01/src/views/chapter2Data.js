// 通讯录谜题逻辑（移入通讯录模块，玩家自主探索触发）
function setupContactsPuzzleInModule(area, ctx) {
  const refreshBtn = area.querySelector('#contactRefreshBtn2');
  const secondChen = area.querySelector('#secondChen2');
  const resultCount = area.querySelector('#contactResultCount2');
  const contactTip = area.querySelector('#contactTip2');
  if (!refreshBtn) return;

  const alreadySolved = ctx.flow.choices && ctx.flow.choices.ch2_contactsMerged;

  // 已通关
  if (alreadySolved) {
    if (secondChen) secondChen.style.display = 'none';
    if (resultCount) resultCount.textContent = '找到 1 条结果';
    if (contactTip) contactTip.style.display = 'block';
    refreshBtn.disabled = true;
    refreshBtn.textContent = '已更新';
    return;
  }

  refreshBtn.addEventListener('click', function () {
    if (ctx.flow.choices && ctx.flow.choices.ch2_contactsMerged) return;

    // 灯闪（物理空间共振）
    document.body.classList.add('fx-lamp');
    setTimeout(function () { document.body.classList.remove('fx-lamp'); }, 250);

    if (secondChen) {
      secondChen.classList.add('merging');
      // 2.5s 后黑闪 + 嗡声骤增
      setTimeout(function () {
        if (ctx.ARG_Horror) ctx.ARG_Horror.blackFlash(240);
        if (ctx.ARG_Audio) ctx.ARG_Audio.humSpike();
      }, 2500);
      // 3s 后完成合并
      setTimeout(function () {
        secondChen.classList.add('merged-out');
        setTimeout(function () {
          secondChen.style.display = 'none';
          if (resultCount) resultCount.textContent = '找到 1 条结果';
          if (contactTip) contactTip.style.display = 'block';

          ctx.flow.choices.ch2_contactsMerged = true;

          // OA 文字模糊 0.3s
          const content = document.querySelector('#ch2Content');
          if (content) {
            content.classList.add('blurred');
            setTimeout(function () { content.classList.remove('blurred'); }, 300);
          }

          // 默言弹窗：第一次"纠正"
          if (ctx.host) showMuteCorrectionInData(ctx.host);

          // 通知主视图更新完成状态
          if (ctx.onPuzzleSolved) ctx.onPuzzleSolved();
        }, 300);
      }, 3000);
    }
  });
}

