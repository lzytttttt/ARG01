// ===================================================================
// 通用列表+详情渲染（支持 italicTitle、simplified、HTML body）
// ===================================================================
function renderList2(area, data, cols, ctx) {
  const colCount = cols.length;
  const table = document.createElement('table');
  table.className = 'mod-list';
  const thead = '<thead><tr>' + cols.map(function (c) { return '<th>' + c + '</th>'; }).join('') + '</tr></thead>';
  const rows = data.map(function (d, i) {
    const cells = cols.map(function (c) {
      const key = {
        '编号': 'no', '日期': 'date', '标题': 'title', '工单号': 'no',
        '议题': 'title', '姓名': 'name', '类型': 'type', '变动': 'type',
        '部门': 'dept', '文件名': 'name', '紧急': 'urgent', '状态': 'status',
        '发文部门': 'dept', '参加人数': 'attendees', '讲师': 'trainer'
      }[c] || c;
      let val = d[key] !== undefined ? d[key] : '';
      // 斜体标题：工单中心中涉及陈海平的工单
      if ((c === '标题' || c === '议题') && d.italicTitle) {
        val = '<i>' + val + '</i>';
      }
      return '<td>' + val + '</td>';
    }).join('');
    return '<tr data-idx="' + i + '">' + cells + '</tr>';
  }).join('');
  table.innerHTML = thead + '<tbody>' + rows + '</tbody>';
  area.appendChild(table);

  const tbody = table.querySelector('tbody');
  let expandedIdx = -1;

  tbody.querySelectorAll('tr[data-idx]').forEach(function (tr) {
    tr.addEventListener('click', function () {
      const idx = parseInt(tr.getAttribute('data-idx'), 10);
      if (isNaN(idx)) return;
      const d = data[idx];
      if (!d) return;

      // 移除已有的展开行
      const existingExpand = tbody.querySelector('.expand-row');
      if (existingExpand) existingExpand.remove();
      tbody.querySelectorAll('tr.is-open').forEach(function (t) { t.classList.remove('is-open'); });

      // 如果点击的是已展开行，折叠
      if (expandedIdx === idx) {
        expandedIdx = -1;
        return;
      }

      expandedIdx = idx;
      tr.classList.add('is-open');

      // 构建展开行的HTML
      let detailHTML = '';

      // 简略显示
      if (d.simplified) {
        detailHTML =
          '<div class="mod-detail" style="margin:0;border-left-color:#ccc;">' +
            '<div class="src">' + (d.no || '') + '</div>' +
            '<div style="color:#999;font-size:14px;padding:6px 0;">' + (d.body || '日常运维工单，无异常情况。') + '</div>' +
          '</div>';
      } else {
        detailHTML += '<div class="mod-detail" style="margin:0;">';
        detailHTML += '<div class="src">' + (d.no || d.name || '') + ' · ' + (d.date || '') + '</div>';
        if (d.dept) detailHTML += '<div class="src" style="font-size:13px;color:#888;">发文部门：' + d.dept + '</div>';
        if (d.submitter) detailHTML += '<div class="src" style="font-size:13px;color:#888;">提交人：' + d.submitter + ' · ' + d.time + '</div>';
        if (d.trainer) detailHTML += '<div class="src" style="font-size:13px;color:#888;">讲师：' + d.trainer + ' · 参加人数：' + d.attendees + '</div>';
        if (d.body) {
          detailHTML += '<div style="white-space:pre-line;line-height:2.1;">' + d.body + '</div>';
        }
        if (d.result) {
          detailHTML += '<div style="margin-top:10px;padding:8px 12px;background:#f0faf0;border:1px solid #b7eb8f;font-size:13px;"><b>处理结果：</b>' + d.result + '</div>';
        }
        if (d.muteNote) {
          detailHTML += '<div style="margin-top:8px;padding:8px 12px;background:#1a2a40;color:#8ab4f8;border:1px solid #2a4060;font-size:13px;"><b>默言旁注：</b>' + d.muteNote + '</div>';
        }
        detailHTML += '</div>';
      }

      // 将展开行插入到被点击行的后面
      const expandTr = document.createElement('tr');
      expandTr.className = 'expand-row';
      expandTr.innerHTML = '<td colspan="' + colCount + '" style="padding:0 12px 14px;background:#fafafa;border-bottom:1px solid #e0e0e0;">' + detailHTML + '</td>';
      tr.parentNode.insertBefore(expandTr, tr.nextSibling);

      // clue 独白单独追加在展开行后面
      if (d.clue) {
        const clueTr = document.createElement('tr');
        clueTr.className = 'expand-row';
        const cl = document.createElement('div');
        cl.className = 'fx-monologue';
        cl.style.cssText = 'margin:6px 0 10px;';
        cl.textContent = d.clue;
        clueTr.innerHTML = '<td colspan="' + colCount + '" style="padding:0 12px 8px;background:#fafafa;border-bottom:1px solid #e0e0e0;"></td>';
        clueTr.querySelector('td').appendChild(cl);
        expandTr.parentNode.insertBefore(clueTr, expandTr.nextSibling);
        requestAnimationFrame(function () { cl.classList.add('show'); });
      }
    });
  });

  const tip = document.createElement('div');
  tip.style.cssText = 'font-size:12px;color:var(--oa-text-light);margin-top:10px;';
  tip.textContent = '共 ' + data.length + ' 份记录。点击任一行查看详情，再次点击收起。';
  area.appendChild(tip);
}

